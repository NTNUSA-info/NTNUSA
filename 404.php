			function openbar()
			{
				var bar = document.getElementById("dropdown-toggle");
				if(bar.style.top === "0em")
				{
					bar.style.top="-100em";
					document.getElementById("aboutus").style.display="none";
					document.getElementById("dpnews").style.display="none";
					bar.style.position="absolute";
				}
				else
				{
					bar.style.position="relative";
					bar.style.top="0em";
					document.getElementById("aboutus").style.display="none";
					document.getElementById("dpnews").style.display="none";
				}
			}

			function op(thing)
			{
				var tar = document.getElementById(thing);
				if(tar.style.display == "none")
				{tar.style.display = "block";}
				else
				{tar.style.display = "none";}
